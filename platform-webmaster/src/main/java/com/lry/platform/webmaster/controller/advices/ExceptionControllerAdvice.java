import com.lry.platform.webmaster.dto.R;
import com.lry.platform.webmaster.exceptions.MyBaseException;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.bind.annotation.ResponseBody;

/**
 * Created by lry on 2021/8/7/23:07
 *
 * @Author lry
 */
@ControllerAdvice
@ResponseBody
public class ExceptionControllerAdvice {
    @ExceptionHandler(MyBaseException.class)
    public R processMyBaseException(MyBaseException exception) {
        return R.error(exception.getCode(), exception.getMessage());
    }

    @ExceptionHandler(Exception.class)
    public R processException(Exception exception) {
        exception.printStackTrace();
        return R.error();
    }
}
